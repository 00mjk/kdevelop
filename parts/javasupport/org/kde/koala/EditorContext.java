package org.kde.koala;

import org.kde.qt.*;
import java.util.*;
import java.lang.Error;

/**

 A context for the popup menu in the editor. In this case,
 linestr() returns the contents of the line in which the
 mouse button has been pressed, and col() returns the column
 number.
 
 @short 
 A context for the popup menu in the editor.
*/
public class EditorContext extends Context  {
	protected EditorContext(Class dummy){super((Class) null);}

	private native void newEditorContext( String linestr, int col);
	public EditorContext( String linestr, int col) {
		super((Class) null);
		newEditorContext( linestr, col);
	}
	protected native void finalize() throws InternalError;
	public native String linestr();
	public native int col();
}
